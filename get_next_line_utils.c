#include <unistd.h>

size_t	ft_strlen(const char *s)
{
	int	i;

	i = 0;
	while (s[i] != '\0')
		i++;
	return (i);
}

void    ft_putendl_fd(char *buffer)
{
    write(1, buffer,ft_strlen(buffer));
}
